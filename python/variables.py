name = "Alice"
age = 30
pi = 3.14159

# Convert to different data types
age_str = str(age)  # Convert int to string
pi_int = int(pi)     # Convert float to int (truncates decimal)

# Print the results
print(f"Name: {name} (string)")
print(f"Age: {age} (int), {age_str} (string)")
print(f"Pi: {pi} (float), {pi_int} (int)")