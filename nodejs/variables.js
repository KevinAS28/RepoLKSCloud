// Create variables
let name = "Alice";
let age = 30;
const pi = 3.14159;  // Use const for constant values like pi

// Convert to different data types
let age_str = age.toString();  // Convert int to string
let pi_int = Math.floor(pi);    // Convert float to int (rounds down)

// Print the results
console.log(`Name: ${name} (string)`);
console.log(`Age: ${age} (int), ${age_str} (string)`);
console.log(`Pi: ${pi} (float), ${pi_int} (int)`);
